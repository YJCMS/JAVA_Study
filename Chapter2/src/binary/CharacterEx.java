package binary;

public class CharacterEx {

	public static void main(String[] args) {
		char ch = 'A';
		
		System.out.println(ch);   // 문자형 출력
		System.out.println((int)ch); 
		
		ch = 66;
		
		System.out.println(ch);
		
		int ch2 = 67;
		
		System.out.println(ch2);  // 정수형 출력
		System.out.println((char)ch2);
		
		
		
	}

}
