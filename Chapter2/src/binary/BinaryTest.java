package binary;

public class BinaryTest {

	public static void main(String[] args) {
		int num = 10;
		int bNum = 0B1010; // 0b는 2진수라는 의미
		int oNum = 012;    // 8진수
		int hNum = 0XA;    // 16진수
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(hNum);

	}

}
